		.section code

; ************************************************************************************************
;
;										DO command
;
; ************************************************************************************************

Command_DO:	;; [do]
		lda 	#STK_DO
		jsr 	StackOpen 
		jsr 	STKSaveCodePosition 		; save loop position
		rts

; ************************************************************************************************
;
;										EXIT command
;
; ************************************************************************************************

Command_EXIT:	;; [exit]
		lda 	#STK_DO 					; check in LOOP
		jsr 	StackCheckFrame
		jsr 	StackClose 					; close it
		lda 	#KWD_LOOP 					; forward to LOOP
		tax
		jsr 	ScanForward
		rts

; ************************************************************************************************
;
;										LOOP command
;
; ************************************************************************************************

Command_LOOP:	;; [loop]
		lda 	#STK_DO 				
		jsr 	StackCheckFrame
		jsr 	STKLoadCodePosition 		; loop back
		rts

		.send code

