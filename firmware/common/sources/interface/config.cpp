void CFGProcess(void) {
#ifdef USBKEY_STORAGE
	if (FIOReadFileBasic(".config",0x100) == 0) {  								// Try to read config file
		LOCSetLocale(cpuMemory[0x100],cpuMemory[0x101]);  						// Set locale from config file.
	}
#endif
}

